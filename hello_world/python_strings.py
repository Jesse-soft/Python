# In python a string is sequence of character enclosed in ' ' or " ".

# Single line
varA = "hello world"

# Multi line
varB = " This is too\
 big to fit\
 on a single line so\
 you multi-lined it"

# index in string from 0
varA[1]
print(varA[1])

# length function in strings
len(varB)
length = len(varB)
print(length)

# concatenation
print(varA+varB)
